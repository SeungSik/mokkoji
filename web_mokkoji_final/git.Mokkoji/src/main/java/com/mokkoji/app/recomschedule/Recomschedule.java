package com.mokkoji.app.recomschedule;

public class Recomschedule {

	private Integer rn;
	private Integer userpn;
	private String typename;
	private String Startdate;
	private String Enddate;
	private Integer weektimes;
	private Integer daytime;
	
	public Integer getRn() {
		return rn;
	}
	public void setRn(Integer rn) {
		this.rn = rn;
	}
	public Integer getUserpn() {
		return userpn;
	}
	public void setUserpn(Integer userpn) {
		this.userpn = userpn;
	}
	public String getTypename() {
		return typename;
	}
	public void setTypname(String typename) {
		this.typename = typename;
	}
	public String getStartdate() {
		return Startdate;
	}
	public void setStartdate(String startdate) {
		Startdate = startdate;
	}
	public String getEnddate() {
		return Enddate;
	}
	public void setEnddate(String enddate) {
		Enddate = enddate;
	}
	public Integer getWeektimes() {
		return weektimes;
	}
	public void setWeektimes(Integer weektimes) {
		this.weektimes = weektimes;
	}
	public Integer getDaytime() {
		return daytime;
	}
	public void setDaytime(Integer daytime) {
		this.daytime = daytime;
	}
	
	
}
